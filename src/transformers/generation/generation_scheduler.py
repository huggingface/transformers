"""
Generation Scheduler — Public scheduler object for fine-grained generation control.

This module provides the `GenerationScheduler` and `SchedulerCallback` classes,
enabling users to observe and intervene in the token generation process.

Three operating modes (configured via ``SchedulerMode``):
    1. **NONE** (default): No scheduler, 100% original HF behavior.
    2. **INTERNAL**: LLM-driven scheduling — the model generates control tokens
       that the scheduler intercepts and executes. The ``ControlTokenParser``
       handles token-to-action mapping.
    3. **FORCE**: External scheduling — external code controls generation via
       the scheduler API (pause, resume, inject, force_token).

Design principles:
    - Zero overhead when not used (default empty scheduler, no callbacks).
    - Fully backward compatible — ``scheduler_mode="none"`` preserves original behavior.
    - No graph breaks for torch.compile (callbacks are called outside the compiled region).
    - Composable with existing LogitsProcessor and StoppingCriteria.
"""

import time
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Union

import torch

from ..utils import logging
from .state_machine import GenerationPhase, GenerationState, GenerationStateMachine, SchedulerMode


logger = logging.get_logger(__name__)


class ControlTokenParser:
    """
    Parses control tokens generated by the LLM in ``INTERNAL`` mode.

    Maps special token IDs to scheduler actions. When the model generates
    a control token, the parser converts it into a scheduler command
    (e.g., pause, inject, switch chunk).

    This is the bridge between ``token → control signal`` in internal mode.

    Args:
        control_tokens (`Dict[int, str]`):
            Mapping from token ID to action name. Example:
            ``{32000: "pause", 32001: "read_chunk", 32002: "recall"}``
        action_handlers (`Dict[str, callable]`, *optional*):
            Mapping from action name to handler function:
            ``(action_name, state, context) -> None``
        strip_control_tokens (`bool`, defaults to `True`):
            Whether to remove control tokens from the generated sequence.

    Example:
        ```python
        parser = ControlTokenParser(
            control_tokens={32000: "pause", 32001: "read_chunk"},
            action_handlers={
                "pause": lambda name, state, ctx: setattr(ctx, 'should_pause', True),
                "read_chunk": my_chunk_handler,
            }
        )
        scheduler = GenerationScheduler(mode=SchedulerMode.INTERNAL, control_token_parser=parser)
        ```
    """

    def __init__(
        self,
        control_tokens: Optional[Dict[int, str]] = None,
        action_handlers: Optional[Dict[str, Any]] = None,
        strip_control_tokens: bool = True,
    ):
        self.control_tokens = control_tokens or {}
        self.action_handlers = action_handlers or {}
        self.strip_control_tokens = strip_control_tokens
        self._inverse_map: Dict[str, int] = {v: k for k, v in self.control_tokens.items()}

    def is_control_token(self, token_id: int) -> bool:
        """Check if a token ID is a registered control token."""
        return token_id in self.control_tokens

    def get_action(self, token_id: int) -> Optional[str]:
        """Get the action name for a control token ID."""
        return self.control_tokens.get(token_id)

    def get_token_id(self, action_name: str) -> Optional[int]:
        """Get the token ID for an action name."""
        return self._inverse_map.get(action_name)

    def execute(self, token_id: int, state: "GenerationState", context: "SchedulerContext") -> bool:
        """
        Execute the action associated with a control token.

        Args:
            token_id: The control token ID.
            state: Current generation state.
            context: Mutable scheduler context.

        Returns:
            `True` if action was handled, `False` if no handler found.
        """
        action = self.get_action(token_id)
        if action is None:
            return False

        handler = self.action_handlers.get(action)
        if handler is not None:
            handler(action, state, context)
            logger.debug(f"ControlTokenParser: executed action '{action}' for token {token_id}")
            return True

        logger.debug(f"ControlTokenParser: no handler for action '{action}' (token {token_id})")
        return False

    def register_action(self, action_name: str, token_id: int, handler: Optional[Any] = None):
        """
        Register a new control token action.

        Args:
            action_name: Name of the action.
            token_id: Token ID that triggers this action.
            handler: Optional handler function.
        """
        self.control_tokens[token_id] = action_name
        self._inverse_map[action_name] = token_id
        if handler is not None:
            self.action_handlers[action_name] = handler

    @classmethod
    def from_tokenizer(
        cls,
        tokenizer,
        control_token_names: List[str],
        action_handlers: Optional[Dict[str, Any]] = None,
        strip_control_tokens: bool = True,
        prefix: str = "<|sched:",
        suffix: str = "|>",
    ) -> "ControlTokenParser":
        """
        Create a ControlTokenParser from a tokenizer, automatically registering
        control tokens as special tokens if they don't already exist.

        This avoids the manual token ID management that Grok's review flagged
        as error-prone. The method:
        1. Constructs token strings from ``control_token_names`` (e.g. ``<|sched:pause|>``).
        2. Adds them to the tokenizer as ``additional_special_tokens`` if absent.
        3. Builds the ``control_tokens`` mapping from the tokenizer's vocabulary.

        **Important**: After calling this method, you should resize the model
        embeddings with ``model.resize_token_embeddings(len(tokenizer))``.

        Args:
            tokenizer: A PreTrainedTokenizer instance.
            control_token_names (`List[str]`):
                Action names (e.g. ``["pause", "read_chunk", "recall"]``).
            action_handlers (`Dict[str, callable]`, *optional*):
                Mapping from action name to handler function.
            strip_control_tokens (`bool`, defaults to `True`):
                Whether to remove control tokens from generated output.
            prefix (`str`, defaults to ``"<|sched:"``):
                Prefix for constructing token strings.
            suffix (`str`, defaults to ``"|>"``):
                Suffix for constructing token strings.

        Returns:
            A configured ``ControlTokenParser``.

        Example:
            ```python
            parser = ControlTokenParser.from_tokenizer(
                tokenizer,
                control_token_names=["pause", "read_chunk", "recall"],
                action_handlers={
                    "pause": lambda name, state, ctx: setattr(ctx, 'should_pause', True),
                    "read_chunk": my_chunk_handler,
                }
            )
            model.resize_token_embeddings(len(tokenizer))
            scheduler = GenerationScheduler(mode="internal", control_token_parser=parser)
            ```
        """
        # Build token strings
        token_strings = {name: f"{prefix}{name}{suffix}" for name in control_token_names}

        # Check which tokens need to be added
        existing_special = set(tokenizer.additional_special_tokens) if hasattr(tokenizer, 'additional_special_tokens') else set()
        new_tokens = [ts for ts in token_strings.values() if ts not in existing_special and ts not in tokenizer.get_vocab()]

        if new_tokens:
            tokenizer.add_special_tokens({"additional_special_tokens": list(existing_special) + new_tokens})
            logger.info(f"ControlTokenParser.from_tokenizer: added {len(new_tokens)} new special tokens to tokenizer")

        # Build control_tokens mapping: token_id -> action_name
        control_tokens = {}
        for name, token_str in token_strings.items():
            token_id = tokenizer.convert_tokens_to_ids(token_str)
            if token_id == tokenizer.unk_token_id:
                logger.warning(f"ControlTokenParser.from_tokenizer: token '{token_str}' resolved to UNK, skipping")
                continue
            control_tokens[token_id] = name

        return cls(
            control_tokens=control_tokens,
            action_handlers=action_handlers or {},
            strip_control_tokens=strip_control_tokens,
        )


@dataclass
class SchedulerContext:
    """
    Mutable context object shared between the scheduler and callbacks.

    Callbacks can modify this context to influence generation behavior.
    The scheduler reads these fields at each step to apply user interventions.

    Attributes:
        mode (`SchedulerMode`):
            Current scheduler operating mode.
        should_pause (`bool`):
            If True, the generation loop will pause after the current step.
        forced_token (`int`, *optional*):
            If set, the next token will be forced to this ID (overrides sampling).
        tokens_to_inject (`List[int]`, *optional*):
            If set, these tokens will be injected into the sequence.
            Note: Injection requires a re-prefill to update the KV Cache.
        logits_modifier (`callable`, *optional*):
            A function `(logits, state) -> logits` applied before LogitsProcessor.
        custom_data (`Dict[str, Any]`):
            Arbitrary user data that persists across steps.
        step_budget (`int`, *optional*):
            Maximum number of decoding steps. None means unlimited.
        check_interval (`int`):
            How often (in steps) to enter the CHECKING phase. 0 means never.
    """

    mode: SchedulerMode = SchedulerMode.NONE
    should_pause: bool = False
    forced_token: Optional[int] = None
    tokens_to_inject: Optional[List[int]] = None
    logits_modifier: Optional[Any] = None  # Callable[[torch.FloatTensor, GenerationState], torch.FloatTensor]
    custom_data: Dict[str, Any] = field(default_factory=dict)
    step_budget: Optional[int] = None
    check_interval: int = 0


class SchedulerCallback:
    """
    Base class for generation scheduler callbacks.

    Users inherit from this class to implement custom generation intervention logic.
    All hook methods return `True` to continue generation, `False` to pause.

    Example:
        ```python
        class ToolCallDetector(SchedulerCallback):
            def on_token_generated(self, token_id, state, context):
                if self._is_tool_call_token(token_id):
                    context.should_pause = True
                    context.custom_data["tool_call_detected"] = True
                    return False  # Pause generation
                return True
        ```

    Available hooks (called in order):
        1. `on_phase_transition` — When the generation phase changes.
        2. `on_step_begin` — Before each decoding step.
        3. `on_logits_ready` — After forward pass, before LogitsProcessor.
        4. `on_token_generated` — After token selection.
        5. `on_step_end` — After each decoding step.
        6. `on_generation_complete` — When generation finishes.
        7. `on_error` — When an error occurs.
    """

    def on_phase_transition(
        self,
        from_phase: GenerationPhase,
        to_phase: GenerationPhase,
        state: GenerationState,
        context: SchedulerContext,
    ) -> bool:
        """
        Called when the generation phase changes.

        Args:
            from_phase: The phase being left.
            to_phase: The phase being entered.
            state: Current generation state snapshot.
            context: Mutable scheduler context.

        Returns:
            `True` to continue, `False` to pause generation.
        """
        return True

    def on_step_begin(
        self,
        state: GenerationState,
        context: SchedulerContext,
    ) -> bool:
        """
        Called at the beginning of each decoding step (before forward pass).

        Args:
            state: Current generation state.
            context: Mutable scheduler context.

        Returns:
            `True` to continue, `False` to skip this step and pause.
        """
        return True

    def on_logits_ready(
        self,
        logits: torch.FloatTensor,
        state: GenerationState,
        context: SchedulerContext,
    ) -> torch.FloatTensor:
        """
        Called after the forward pass produces logits, before LogitsProcessor.

        This is more powerful than `LogitsProcessor` because it has access to the
        full generation state and can modify the scheduler context.

        Args:
            logits: Raw logits from the model, shape `(batch_size, vocab_size)`.
            state: Current generation state.
            context: Mutable scheduler context.

        Returns:
            Modified logits (must have the same shape).
        """
        return logits

    def on_token_generated(
        self,
        token_id: torch.LongTensor,
        state: GenerationState,
        context: SchedulerContext,
    ) -> bool:
        """
        Called after a token is selected (before it's appended to input_ids).

        Args:
            token_id: The selected token ID(s), shape `(batch_size,)`.
            state: Current generation state.
            context: Mutable scheduler context.

        Returns:
            `True` to continue, `False` to pause generation.
        """
        return True

    def on_step_end(
        self,
        state: GenerationState,
        context: SchedulerContext,
    ) -> bool:
        """
        Called at the end of each decoding step (after token is appended).

        Args:
            state: Updated generation state.
            context: Mutable scheduler context.

        Returns:
            `True` to continue, `False` to pause generation.
        """
        return True

    def on_generation_complete(
        self,
        state: GenerationState,
    ):
        """
        Called when generation finishes normally.

        Args:
            state: Final generation state.
        """
        pass

    def on_error(
        self,
        error: Exception,
        state: GenerationState,
    ):
        """
        Called when an error occurs during generation.

        Args:
            error: The exception that was raised.
            state: Generation state at the time of the error.
        """
        pass


class GenerationScheduler:
    """
    Public scheduler object for fine-grained generation control.

    The scheduler wraps a `GenerationStateMachine` and provides:
        1. User-callable intervention APIs (pause, resume, inject, force_token).
        2. Callback registration and dispatch.
        3. State snapshot serialization/deserialization.
        4. Three operating modes: NONE, INTERNAL, FORCE.

    **Mode: NONE (default)**
        No scheduler intervention. 100% original HF behavior.

    **Mode: INTERNAL (LLM-driven)**
        ```python
        parser = ControlTokenParser(control_tokens={32000: "pause"})
        scheduler = GenerationScheduler(mode="internal", control_token_parser=parser)
        output = model.generate(input_ids, scheduler=scheduler)
        ```

    **Mode: FORCE (external control)**
        ```python
        scheduler = GenerationScheduler(mode="force")
        scheduler.register_callback(MyCallback())
        output = model.generate(input_ids, scheduler=scheduler)
        ```

    Attributes:
        state_machine (`GenerationStateMachine`):
            The underlying state machine managing phase transitions.
        context (`SchedulerContext`):
            Mutable context shared with callbacks.
        mode (`SchedulerMode`):
            Current operating mode.
        control_token_parser (`ControlTokenParser`, *optional*):
            Parser for control tokens in INTERNAL mode.
    """

    def __init__(
        self,
        generation_config=None,
        mode: Union[str, SchedulerMode] = SchedulerMode.NONE,
        control_token_parser: Optional[ControlTokenParser] = None,
    ):
        """
        Initialize the scheduler.

        Args:
            generation_config: Optional GenerationConfig for scheduler-specific settings.
            mode: Operating mode — "none", "internal", or "force" (or a `SchedulerMode` enum).
            control_token_parser: Parser for control tokens (required for INTERNAL mode).
        """
        # Resolve mode from string
        if isinstance(mode, str):
            mode = SchedulerMode(mode.lower())
        self._mode = mode

        self.state_machine = GenerationStateMachine()
        self.config = generation_config
        self.control_token_parser = control_token_parser
        self._callbacks: List[SchedulerCallback] = []
        self._context = SchedulerContext(mode=self._mode)
        self._is_active: bool = False
        self._start_time: Optional[float] = None
        self._step_times: List[float] = []

        # Validate: INTERNAL mode should have a control_token_parser
        if self._mode == SchedulerMode.INTERNAL and self.control_token_parser is None:
            logger.warning(
                "GenerationScheduler initialized in INTERNAL mode without a ControlTokenParser. "
                "Control token detection will be disabled. Pass a ControlTokenParser for full functionality."
            )

    # ==================== Properties ====================

    @property
    def mode(self) -> SchedulerMode:
        """Current scheduler operating mode."""
        return self._mode

    @property
    def context(self) -> SchedulerContext:
        """The mutable scheduler context."""
        return self._context

    @property
    def phase(self) -> GenerationPhase:
        """Current generation phase."""
        return self.state_machine.phase

    @property
    def is_active(self) -> bool:
        """Whether the scheduler is currently managing a generation."""
        return self._is_active

    @property
    def is_enabled(self) -> bool:
        """Whether the scheduler is enabled (mode != NONE)."""
        return self._mode != SchedulerMode.NONE

    @property
    def callbacks(self) -> List[SchedulerCallback]:
        """Registered callbacks (read-only view)."""
        return list(self._callbacks)

    # ==================== User API ====================

    def pause(self):
        """
        Pause generation after the current step.

        The generation loop will break at the next pause check point.
        Use `resume()` to continue.
        """
        self._context.should_pause = True
        logger.debug("Scheduler: pause requested")

    def resume(self):
        """
        Resume a paused generation.

        Clears the pause flag so the generation loop continues.
        """
        self._context.should_pause = False
        logger.debug("Scheduler: resume requested")

    def force_next_token(self, token_id: int):
        """
        Force the next generated token to be a specific token ID.

        This overrides the sampling/greedy selection for exactly one step.
        The forced token is cleared after it's consumed.

        Args:
            token_id: The token ID to force.
        """
        self._context.forced_token = token_id
        logger.debug(f"Scheduler: forced next token = {token_id}")

    def inject_tokens(self, token_ids: List[int]):
        """
        Schedule token injection at the current position.

        The injected tokens will be appended to `input_ids` and the model
        will re-run a prefill pass to update the KV Cache for these tokens.

        **Important**: Token injection triggers a re-prefill, which is more
        expensive than a single decode step. Use sparingly.

        Args:
            token_ids: List of token IDs to inject.
        """
        self._context.tokens_to_inject = list(token_ids)
        logger.debug(f"Scheduler: token injection scheduled ({len(token_ids)} tokens)")

    def get_state(self) -> GenerationState:
        """
        Get the current generation state snapshot.

        Returns:
            A `GenerationState` with the current generation progress.
        """
        return self.state_machine.current_state

    def is_complete(self) -> bool:
        """
        Whether generation has finished.

        Returns:
            `True` if the state machine is in COMPLETE or ERROR phase.
        """
        return self.state_machine.is_terminal()

    def is_paused(self) -> bool:
        """
        Whether the scheduler is currently paused.

        Returns:
            `True` if a pause has been requested.
        """
        return self._context.should_pause

    # ==================== State Persistence ====================

    def save_checkpoint(self, path: Optional[str] = None) -> Dict[str, Any]:
        """
        Serialize the full scheduler state for checkpointing.

        Args:
            path: Optional file path. If provided, saves to disk.

        Returns:
            A dict containing the serialized state.
        """
        state = self.state_machine.current_state
        checkpoint = {
            "phase": state.phase.name,
            "step": state.step,
            "context": {
                "should_pause": self._context.should_pause,
                "forced_token": self._context.forced_token,
                "step_budget": self._context.step_budget,
                "check_interval": self._context.check_interval,
                "custom_data": self._context.custom_data,
            },
            "metadata": state.metadata,
            "history": [(f.name, t.name, ts) for f, t, ts in self.state_machine.history],
        }

        if path is not None:
            state.save(path)

        return checkpoint

    def load_checkpoint(self, checkpoint: Union[str, Dict[str, Any]], device: Optional[torch.device] = None):
        """
        Restore scheduler state from a checkpoint.

        Args:
            checkpoint: Either a file path (str) or a dict from `save_checkpoint()`.
            device: Device to move tensors to.
        """
        if isinstance(checkpoint, str):
            state = GenerationState.load(checkpoint, device=device)
            self.state_machine.current_state = state
        else:
            # Restore from dict
            phase = GenerationPhase[checkpoint["phase"]]
            self.state_machine._phase = phase
            self.state_machine._state.phase = phase
            self.state_machine._state.step = checkpoint["step"]
            self.state_machine._state.metadata = checkpoint.get("metadata", {})

            ctx = checkpoint.get("context", {})
            self._context.should_pause = ctx.get("should_pause", False)
            self._context.forced_token = ctx.get("forced_token", None)
            self._context.step_budget = ctx.get("step_budget", None)
            self._context.check_interval = ctx.get("check_interval", 0)
            self._context.custom_data = ctx.get("custom_data", {})

        self._is_active = True
        logger.info(f"Scheduler: checkpoint loaded (phase={self.phase.name})")

    # ==================== Callback Management ====================

    def register_callback(self, callback: SchedulerCallback):
        """
        Register a callback to receive generation events.

        Args:
            callback: A `SchedulerCallback` instance.
        """
        if not isinstance(callback, SchedulerCallback):
            raise TypeError(f"Expected SchedulerCallback, got {type(callback).__name__}")
        self._callbacks.append(callback)
        logger.debug(f"Scheduler: registered callback {type(callback).__name__}")

    # Alias for convenience (as suggested by reviewers)
    add_callback = register_callback

    def remove_callback(self, callback: SchedulerCallback):
        """
        Remove a previously registered callback.

        Args:
            callback: The callback instance to remove.
        """
        self._callbacks.remove(callback)
        logger.debug(f"Scheduler: removed callback {type(callback).__name__}")

    def clear_callbacks(self):
        """Remove all registered callbacks."""
        self._callbacks.clear()

    # ==================== Internal Methods (called by generate()) ====================

    def _activate(self):
        """Called when generate() starts using this scheduler."""
        if self._mode == SchedulerMode.NONE:
            return  # No-op for NONE mode
        self._is_active = True
        self._start_time = time.time()
        self._step_times.clear()
        self.state_machine.reset()
        self._context.mode = self._mode
        logger.debug(f"Scheduler activated in {self._mode.value} mode")

    def _deactivate(self):
        """Called when generate() finishes."""
        self._is_active = False

    def _notify_phase_transition(
        self, from_phase: GenerationPhase, to_phase: GenerationPhase, state: GenerationState
    ) -> bool:
        """
        Notify all callbacks of a phase transition.

        Args:
            from_phase: The phase being left.
            to_phase: The phase being entered.
            state: Current generation state.

        Returns:
            `True` if all callbacks allow continuation, `False` if any requests pause.
        """
        self.state_machine.transition_to(to_phase)
        state.phase = to_phase

        if not self._callbacks:
            return True

        for cb in self._callbacks:
            try:
                if not cb.on_phase_transition(from_phase, to_phase, state, self._context):
                    return False
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_phase_transition raised: {e}")
                self._notify_error(e, state)
                return False
        return True

    def _notify_step_begin(self, state: GenerationState) -> bool:
        """
        Notify callbacks at the beginning of a decoding step.

        Returns:
            `True` to proceed, `False` to skip/pause.
        """
        if not self._callbacks:
            return True

        for cb in self._callbacks:
            try:
                if not cb.on_step_begin(state, self._context):
                    return False
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_step_begin raised: {e}")
                return False
        return True

    def _notify_logits_ready(
        self, logits: torch.FloatTensor, state: GenerationState
    ) -> torch.FloatTensor:
        """
        Pass logits through all callbacks for potential modification.

        Args:
            logits: Raw logits from the model.
            state: Current generation state.

        Returns:
            Potentially modified logits.
        """
        if not self._callbacks:
            return logits

        for cb in self._callbacks:
            try:
                logits = cb.on_logits_ready(logits, state, self._context)
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_logits_ready raised: {e}")
        return logits

    def _notify_token_generated(
        self, token_id: torch.LongTensor, state: GenerationState
    ) -> bool:
        """
        Notify callbacks that a token was generated.

        Args:
            token_id: The generated token ID(s).
            state: Current generation state.

        Returns:
            `True` to continue, `False` to pause.
        """
        if not self._callbacks:
            return True

        for cb in self._callbacks:
            try:
                if not cb.on_token_generated(token_id, state, self._context):
                    return False
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_token_generated raised: {e}")
                return False
        return True

    def _notify_step_end(self, state: GenerationState) -> bool:
        """
        Notify callbacks at the end of a decoding step.

        Returns:
            `True` to continue, `False` to pause.
        """
        step_time = time.time()
        self._step_times.append(step_time)

        if not self._callbacks:
            return True

        for cb in self._callbacks:
            try:
                if not cb.on_step_end(state, self._context):
                    return False
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_step_end raised: {e}")
                return False
        return True

    def _notify_generation_complete(self, state: GenerationState):
        """Notify callbacks that generation is complete."""
        for cb in self._callbacks:
            try:
                cb.on_generation_complete(state)
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_generation_complete raised: {e}")

    def _notify_error(self, error: Exception, state: GenerationState):
        """Notify callbacks of an error."""
        for cb in self._callbacks:
            try:
                cb.on_error(error, state)
            except Exception as e:
                logger.warning(f"Callback {type(cb).__name__}.on_error raised: {e}")

    def _should_check(self, step: int) -> bool:
        """
        Whether to enter CHECKING phase at this step.

        Returns:
            `True` if `check_interval > 0` and `step % check_interval == 0`.
        """
        interval = self._context.check_interval
        return interval > 0 and step > 0 and step % interval == 0

    def _should_pause(self) -> bool:
        """Whether a pause has been requested."""
        return self._context.should_pause

    def _has_forced_token(self) -> bool:
        """Whether a forced token is pending."""
        return self._context.forced_token is not None

    def _consume_forced_token(self) -> Optional[int]:
        """Consume and return the forced token (if any)."""
        token = self._context.forced_token
        self._context.forced_token = None
        return token

    def _has_injection(self) -> bool:
        """Whether token injection is pending."""
        return self._context.tokens_to_inject is not None and len(self._context.tokens_to_inject) > 0

    def _consume_injection(self) -> Optional[List[int]]:
        """Consume and return the injection tokens (if any)."""
        tokens = self._context.tokens_to_inject
        self._context.tokens_to_inject = None
        return tokens

    def _check_step_budget(self, step: int) -> bool:
        """
        Whether the step budget has been exhausted.

        Returns:
            `True` if generation should continue, `False` if budget exceeded.
        """
        if self._context.step_budget is None:
            return True
        return step < self._context.step_budget

    def _handle_internal_token(self, token_id: int, state: GenerationState) -> bool:
        """
        Handle a token in INTERNAL mode — check if it's a control token.

        Args:
            token_id: The generated token ID (scalar).
            state: Current generation state.

        Returns:
            `True` if the token is a control token and was handled, `False` otherwise.
        """
        if self._mode != SchedulerMode.INTERNAL:
            return False
        if self.control_token_parser is None:
            return False
        if not self.control_token_parser.is_control_token(token_id):
            return False

        return self.control_token_parser.execute(token_id, state, self._context)

    def _should_strip_token(self, token_id: int) -> bool:
        """
        Whether a token should be stripped from the output (control token in INTERNAL mode).

        Args:
            token_id: The token ID to check.

        Returns:
            `True` if the token should be removed from the generated sequence.
        """
        if self._mode != SchedulerMode.INTERNAL:
            return False
        if self.control_token_parser is None:
            return False
        return (
            self.control_token_parser.is_control_token(token_id)
            and self.control_token_parser.strip_control_tokens
        )

    # ==================== Statistics ====================

    def get_stats(self) -> Dict[str, Any]:
        """
        Get scheduler statistics.

        Returns:
            Dict with timing, mode, and transition statistics.
        """
        total_time = time.time() - self._start_time if self._start_time else 0
        state = self.state_machine.current_state
        return {
            "mode": self._mode.value,
            "phase": self.phase.name,
            "step": state.step,
            "total_time_seconds": total_time,
            "num_callbacks": len(self._callbacks),
            "num_transitions": self.state_machine.transition_count,
            "is_paused": self._context.should_pause,
            "step_budget": self._context.step_budget,
            "check_interval": self._context.check_interval,
        }

    def __repr__(self) -> str:
        state = self.state_machine.current_state
        return (
            f"GenerationScheduler("
            f"mode={self._mode.value}, "
            f"phase={self.phase.name}, "
            f"step={state.step}, "
            f"callbacks={len(self._callbacks)}, "
            f"active={self._is_active})"
        )
