def _get_unpad_data(attention_mask):
    seqlens_in_batch = attention_mask.sum(dim=-1, dtype=torch.int32)
    indices = torch.nonzero(attention_mask.flatten(), as_tuple=False).flatten()
    max_seqlen_in_batch = seqlens_in_batch.max().item()
    cu_seqlens = F.pad(torch.cumsum(seqlens_in_batch, dim=0, dtype=torch.torch.int32), (1, 0))
    return (
        indices,
        cu_seqlens,
        max_seqlen_in_batch,
    )


class WeightQuant(torch.autograd.Function):
    @staticmethod
    @torch.compile
    def forward(ctx, x):
        dtype = x.dtype
        x = x.float()
        s = 1.0 / x.abs().mean().clamp_(min=1e-5)
        x = (x * s).round().clamp(-1, 1) / s
        return x.to(dtype)

    @staticmethod
    def backward(ctx, grad_output):
        grad_input = grad_output.clone()
        return grad_input


class ActQuant(torch.autograd.Function):
    @staticmethod
    @torch.compile
    def forward(ctx, x):
        dtype = x.dtype
        x = x.float()
        s = 127 / x.abs().max(dim=-1, keepdim=True).values.clamp_(min=1e-5)
        x = (x * s).round().clamp(-128, 127) / s
        return x.to(dtype)

    @staticmethod
    def backward(ctx, grad_output):
        grad_input = grad_output.clone()
        return grad_input


class BitLinear(nn.Linear):
    def __init__(
        self,
        in_features: int,
        out_features: int,
        bias: bool = True,
        device=None,
        dtype=None,
        online_quant: bool = False,
    ):
        super(BitLinear, self).__init__(in_features, out_features, bias)
        self.online_quant = online_quant
        if not online_quant:
            self.register_buffer(
                "weight_scale",
                torch.ones(
                    (1),
                    dtype=dtype,
                    device=device,
                ),
            )
            self._register_load_state_dict_pre_hook(self.load_hook)
            self._register_state_dict_hook(self.save_hook)

    def load_hook(
        self,
        state_dict,
        prefix,
        *args,
        **kwargs,
    ):
        if (prefix + "weight") in state_dict:
            state_dict[prefix + "weight"] = unpack_weights(state_dict[prefix + "weight"], dtype=self.weight.dtype)
        return state_dict

    def save_hook(
        self,
        state_dict,
        *args,
        **kwargs,
    ):
        for k, v in state_dict.items():
            if k.endswith("weight"):
                state_dict[k] = pack_weights(state_dict[k] / self.weight_scale)

    def forward(self, input):
        if self.online_quant:
            weight = WeightQuant.apply(self.weight)
        else:
            weight = self.weight
        input = ActQuant.apply(input)
        output = F.linear(input, weight, self.bias)
        if not self.online_quant:
            output = output * self.weight_scale
        return output


class BitNetRMSNorm(nn.Module):
    def __init__(self, hidden_size, eps=1e-5):
        """
        BitNetRMSNorm is equivalent to T5LayerNorm
        """
        super().__init__()
        self.weight = nn.Parameter(torch.ones(hidden_size))
        self.variance_epsilon = eps

    def forward(self, hidden_states):
        input_dtype = hidden_states.dtype
        hidden_states = hidden_states.to(torch.float32)
        variance = hidden_states.pow(2).mean(-1, keepdim=True)
        hidden_states = hidden_states * torch.rsqrt(variance + self.variance_epsilon)
        return self.weight * hidden_states.to(input_dtype)


