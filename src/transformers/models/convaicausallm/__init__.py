from typing import TYPE_CHECKING

from ...utils import (
    OptionalDependencyNotAvailable,
    _LazyModule,
    is_sentencepiece_available,
    is_torch_available,
)


_import_structure = {
    "configuration_convaicausallm": ["CONVAICAUSALLM_PRETRAINED_CONFIG_ARCHIVE_MAP", "ConvaiCausalLMConfig"],
}

try:
    if not is_sentencepiece_available():
        raise OptionalDependencyNotAvailable()
except OptionalDependencyNotAvailable:
    pass
else:
    _import_structure["tokenization_convaicausallm"] = ["ConvaiCausalLMTokenizer"]

try:
    if not is_torch_available():
        raise OptionalDependencyNotAvailable()
except OptionalDependencyNotAvailable:
    pass
else:
    # Point to the modeling file that will be generated by the converter
    _import_structure["modeling_convaicausallm"] = [
        "ConvaiCausalLMForCausalLM",
        "ConvaiCausalLMModel",
        "ConvaiCausalLMPreTrainedModel",
    ]


if TYPE_CHECKING:
    from .configuration_convaicausallm import CONVAICAUSALLM_PRETRAINED_CONFIG_ARCHIVE_MAP, ConvaiCausalLMConfig

    try:
        if not is_sentencepiece_available():
            raise OptionalDependencyNotAvailable()
    except OptionalDependencyNotAvailable:
        pass
    else:
        from .tokenization_convaicausallm import ConvaiCausalLMTokenizer

    try:
        if not is_torch_available():
            raise OptionalDependencyNotAvailable()
    except OptionalDependencyNotAvailable:
        pass
    else:
        # Import from the (to be generated) modeling file for type checking
        from .modeling_convaicausallm import (
            ConvaiCausalLMForCausalLM,
            ConvaiCausalLMModel,
            ConvaiCausalLMPreTrainedModel,
        )

else:
    import sys

    sys.modules[__name__] = _LazyModule(__name__, globals()["__file__"], _import_structure, module_spec=__spec__)
